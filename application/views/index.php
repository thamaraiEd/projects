window.addEventListener("load", function(event)
{
	
	//alert(/^((?!chrome|android).)*safari/i.test(navigator.userAgent));
	
	if ( /^((?!chrome|android).)*safari/i.test(navigator.userAgent)) 
	{ 
		lazymac(); 
		$(".inside_banner").removeClass("inside_banner_win");
		$(".inside_banner").addClass("inside_banner_mac");
	}
	else if (navigator.userAgent.indexOf('MSIE') != -1)
	{
		lazymac();
		$(".inside_banner").removeClass("inside_banner_win");
		$(".inside_banner").addClass("inside_banner_mac");
	}
	else if (navigator.userAgent.indexOf('Trident') != -1 && navigator.userAgent.indexOf('MSIE') == -1 )
	{
		lazymac();
		$(".inside_banner").removeClass("inside_banner_win");
		$(".inside_banner").addClass("inside_banner_mac");
	}
	else
	{
		lazywin();
		$(".inside_banner").addClass("inside_banner_win");
		$(".inside_banner").removeClass("inside_banner_mac");
	}
});
